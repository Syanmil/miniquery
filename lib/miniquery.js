const SweetSelector = {
  select: function(selected){
    if(selected[0] == '#'){
      console.log("id"+selected.slice(1));
      return document.getElementById(selected.slice(1))
    } else if(selected[0] == '.'){
      console.log("class");
      return document.getElementsByClassName(selected.slice(1))
    } else {
      console.log('element');
      return document.getElementsByName(selected)
    }
  }
}

